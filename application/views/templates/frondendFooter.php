							<p>${suggestion.name}</p>
						</div>

						<div class="form-floating mb-2">
							<label for="form-control">Alamat</label>
							<p>${suggestion.alamat}</p>
						</div>`
    				);
    			}
    		});
    		setInterval(function () {
    			if ($("#floatingCID").val() == "") {
    				$(".datanePelanggan").html(null);
					$("#cid").val(null);
    			} else {
    				var datane = $("#floatingCID").val();
    				$.ajax({
    					url: "<?= base_url(); ?>pelayanan/getChekDPelangan",
    					data: {
    						query: datane,
    					},
    					method: "POST",
    					dataType: "json",
    					success: function (suggestion) {
							console.log(suggestion.length);
    						if (suggestion == 0) {
    							$(".datanePelanggan").html(null);
    						}
    					}
    				});
    			}
    		}, 1000);

    	})

